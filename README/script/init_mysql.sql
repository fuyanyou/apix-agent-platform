-- Stored Procedure: append_message
DROP PROCEDURE IF EXISTS append_message;
DELIMITER $$

CREATE PROCEDURE append_message (
    IN p_user_uid VARCHAR(64),
    IN p_conversation_uid VARCHAR(64),
    IN p_role ENUM('human', 'ai', 'system', 'tools', 'info'),
    IN p_content LONGTEXT,
    IN p_think LONGTEXT,
    IN p_extra JSON,
    IN p_info JSON,
    IN p_generation_id VARCHAR(64),
    IN p_node_id VARCHAR(32),
    IN p_parent_id VARCHAR(32),
    IN p_msg_timestamp BIGINT
)
BEGIN
    DECLARE v_conversation_id BIGINT;
    DECLARE v_next_cursor BIGINT;
    DECLARE v_latest_timestamp BIGINT;
    DECLARE v_not_found INT DEFAULT 0;
    DECLARE v_message_id BIGINT;

    -- Handler: SELECT ... INTO returns NOT FOUND when no row matched
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET v_not_found = 1;

    START TRANSACTION;

    -- 1. Lock conversation row
    SELECT
        id,
        latest_cursor,
        latest_timestamp
    INTO
        v_conversation_id,
        v_next_cursor,
        v_latest_timestamp
    FROM conversations
    WHERE user_uid = p_user_uid
      AND conversation_uid = p_conversation_uid
      AND is_deleted = FALSE
    FOR UPDATE;

    IF v_not_found = 1 THEN
        ROLLBACK;
        SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Conversation not found or deleted';
    END IF;

    -- 2. Advance cursor (monotonic)
    SET v_next_cursor = v_next_cursor + 1;

    -- 3. Insert message
    INSERT INTO messages (
        user_uid,
        conversation_id,
        conversation_uid,
        role,
        content,
        think,
        extra,
        info,
        msg_cursor,
        generation_id,
        node_id,
        parent_id,
        msg_timestamp
    )
    VALUES (
        p_user_uid,
        v_conversation_id,
        p_conversation_uid,
        p_role,
        p_content,
        p_think,
        p_extra,
        p_info,
        v_next_cursor,
        p_generation_id,
        p_node_id,
        p_parent_id,
        p_msg_timestamp
    );

    SET v_message_id = LAST_INSERT_ID();

    -- 4. Update conversation (no strict timestamp monotonicity)
    UPDATE conversations
    SET
        latest_cursor = v_next_cursor,
        latest_timestamp = GREATEST(
            IFNULL(latest_timestamp, p_msg_timestamp),
            p_msg_timestamp
        ),
        last_active_at = CURRENT_TIMESTAMP,
        has_new_message = IF(p_role = 'human', FALSE, TRUE)
    WHERE id = v_conversation_id;

    -- 5. Return result
    SELECT
        v_message_id AS msg_id,
        v_next_cursor AS msg_cursor,
        p_msg_timestamp AS msg_timestamp,
        NOW() AS created_at;

    COMMIT;
END$$

